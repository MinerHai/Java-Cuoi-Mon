    void loadData() {
        DonHang dh = donhang_controller.getDonHang(id_donhang);
        KhachHang kh = kh_controller.getKH(dh.getMaKhachHang());
        XeVanChuyen xe = pt_controller.getPhuongTien(donhang_controller.get_CTVanChuyen(id_donhang).getMaxe());
        lbBienSo.setText(xe.getBienSo());
        lbDiaChi.setText(kh.getDiachi());
        lbGhiChu.setText(dh.getGhiChu());
        lbLoaiPT.setText(xe.getLoaiXe());
        lbMaDonHang.setText(String.format("%d", dh.getMaDonHang()));
        lbMaKH.setText(String.format("%d", kh.getMaKhachHang()));
        lbMaPT.setText(String.format("%d", xe.getMaXe()));
        lbNgayNhan.setText(dh.getNgayDatHang().toString());
        lbSDT.setText(kh.getSdt());
        lbTenKH.setText(kh.getTenKhachHang());
        lbTenTaiXe.setText(xe.getTaiXe());
        if (dh.getTrangThai().equals("Đang vận chuyển")) {
            rbDangVanChuyen.setSelected(true);
        } else if (dh.getTrangThai().equals("Giao thành công")) {
            rbGiaoThanhCong.setSelected(true);
        } else {
            rbChoXacNhan.setSelected(true);
        }
    }

