    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        DonHang dh = donhang_controller.getDonHang(id_donhang);
        if (rbChoXacNhan.isSelected()) {
            dh.setTrangThai(rbChoXacNhan.getText());
        } else if (rbDangVanChuyen.isSelected()) {
            dh.setTrangThai(rbDangVanChuyen.getText());
        } else {
            dh.setTrangThai(rbGiaoThanhCong.getText());
        }
       
        if(donhang_controller.edit_donhang(dh)){
             JOptionPane.showMessageDialog(this,"Lưu thành công");
        }
        else{
            JOptionPane.showMessageDialog(this, "Có lỗi xảy ra!!");
        }
        this.dispose();
    }//GEN-LAST:event_btnLuuActionPerformed

