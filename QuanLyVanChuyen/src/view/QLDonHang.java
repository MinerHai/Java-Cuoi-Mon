    private void kiemTraNgay() {
        String inputDate = txtNgayNhan.getText().trim();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        sdf.setLenient(false);  // Không cho phép ngày sai

        try {
            sdf.parse(inputDate);
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Ngày không hợp lệ! Vui lòng nhập đúng định dạng yyyy-MM-dd. ví dụ (2004-08-15)");
        }
    }

